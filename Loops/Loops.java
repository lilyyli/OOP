public class Loops
{
    // instance variables - replace the example below with your own
    

    /**
     * Constructor for objects of class Loops
     */
    public Loops()
    {
        
    }

    public void showExampleLoops()
    {
        for (int i = 15; i < 20; i++)
        {
            System.out.println(i);
        }
        
        System.out.println("");
        
        for (int i = 65; i> 60; i--)
        {
            System.out.println(i);
        }
        
        System.out.println("");
    }
}
